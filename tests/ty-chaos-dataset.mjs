function normalize(value) {
  return value.toLowerCase();
}

function includesAny(value, needles) {
  const lower = normalize(value);
  return needles.some((needle) => lower.includes(normalize(needle)));
}

function resultText(result) {
  return [result.memory?.content, result.memory?.rationale, result.source.content, result.sourceSnippet]
    .filter(Boolean)
    .join("\n");
}

function sourceTypeFor(note) {
  if (note.startsWith("link")) {
    return "link";
  }

  if (note.startsWith("Project:") || note.startsWith("Document")) {
    return "document";
  }

  if (note.includes("Met ") || note.includes("Conference")) {
    return "contact";
  }

  return "text";
}

export async function seedTyChaosDataset({ dbModule, extractionModule }) {
  const sources = [];
  const extractionResults = [];

  for (const note of tyChaosNotes) {
    const source = dbModule.createSourceItem(note, sourceTypeFor(note));
    sources.push(source);
    extractionResults.push(await extractionModule.extractAndStoreMemoriesForSource(source));
  }

  return {
    sources,
    extractionResults,
    memories: extractionResults.flatMap((result) => result.memories),
    openLoops: dbModule.listOpenCommitments()
  };
}

export async function evaluateTyChaosRecall({ recall, memories }) {
  const weakConfidenceScores = memories
    .filter((memory) => memory.confidence < 88)
    .map((memory) => ({
      memoryId: memory.id,
      sourceItemId: memory.sourceItemId,
      kind: memory.kind,
      confidence: memory.confidence,
      status: memory.status,
      content: memory.content
    }));

  const queryReports = await Promise.all(tyChaosQueries.map(async (query) => {
    const results = await recall(query.question, 10);
    const missing = query.expectedNeedles.filter((needle) => !results.some((result) => includesAny(resultText(result), [needle])));
    const falsePositives = results
      .filter((result) => !includesAny(resultText(result), query.allowedNeedles))
      .map((result) => ({
        sourceId: result.source.id,
        kind: result.memory?.kind ?? "source",
        text: result.memory?.content ?? result.sourceSnippet
      }));
    const ambiguous = (query.ambiguityNeedles ?? [])
      .filter((needle) => results.some((result) => includesAny(resultText(result), [needle])))
      .map((needle) => ({ needle, resultCount: results.filter((result) => includesAny(resultText(result), [needle])).length }));
    const missingContext = query.contextNeedles.filter((needle) => !results.some((result) => includesAny(resultText(result), [needle])));

    return {
      id: query.id,
      question: query.question,
      resultCount: results.length,
      missing,
      falsePositives,
      ambiguous,
      missingContext,
      topResults: results.slice(0, 5).map((result) => ({
        sourceId: result.source.id,
        kind: result.memory?.kind ?? "source",
        confidence: result.memory?.confidence ?? null,
        status: result.memory?.status ?? null,
        memory: result.memory?.content ?? null,
        sourceSnippet: result.sourceSnippet
      }))
    };
  }));

  return {
    noteCount: tyChaosNotes.length,
    queryCount: tyChaosQueries.length,
    misses: queryReports.flatMap((report) =>
      report.missing.map((needle) => ({ queryId: report.id, question: report.question, missing: needle }))
    ),
    falsePositives: queryReports.flatMap((report) =>
      report.falsePositives.map((falsePositive) => ({
        queryId: report.id,
        question: report.question,
        ...falsePositive
      }))
    ),
    ambiguousRetrievals: queryReports.flatMap((report) =>
      report.ambiguous.map((ambiguous) => ({
        queryId: report.id,
        question: report.question,
        ...ambiguous
      }))
    ),
    missingContext: queryReports.flatMap((report) =>
      report.missingContext.map((missing) => ({ queryId: report.id, question: report.question, missing }))
    ),
    weakConfidenceScores,
    queryReports
  };
}
